str = 'cpuCode'

# 输出字符串
print("str : ", str)

# 输出第一个到倒数第二个的所有字符
print("str[0:-1] : ", str[0:-1])

# 输出字符串第一个字符
print("str[0] : ", str[0])

# 输出从第三个开始到第五个的字符
print("str[3:6] : ", str[3:6])

# 输出从第三个开始的后的所有字符
print("str[3:] : ", str[3:])

# 输出字符串两次，也可以写成 print (2 * str)
print("str * 2 : ", str * 2)

# 连接字符串
print("str + \"_Test\" : ", str + "_Test")
